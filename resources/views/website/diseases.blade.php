                    <h6>سرطان الدماغ - Brain cancer</h6>
                    <p>سرطان الدماغ يعني تكاثر خلايا غير طبيعية في أنسجة المخ.على الرغم من أنه من المتبع تسمية مثل هذه
                        الأورام باسم عام وشامل وهو أورام الدماغ، إلا أنه ليس بالضرورة أن تكون كل هذه الأورام التي تنشأ
