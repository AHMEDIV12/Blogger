                    <p>شلل دماغي هو اسم عام وشامل للنتائج المترتبة عن خلل أو ضرر في الدماغ في طور النمو، ويسبب الشلل
                        الدماغي درجات مختلفة من الاضطرابات في قدرات الطفل الحركية وفي أدائه، والاضطرابات الحسية</p>
